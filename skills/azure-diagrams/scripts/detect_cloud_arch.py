# DETECTION LOGIC
# =============================================================================

def detect_cloud_content(title: str) -> dict:
    """
    Analyze a slide title/description for cloud architecture content.

    Returns:
        dict with keys: needs_diagram, diagram_type, suggested_pattern,
        cloud_services, confidence
    """
    title_lower = title.lower()

    # Detect cloud services mentioned
    found_services = []
    for service in ALL_CLOUD_SERVICES:
        if service.lower() in title_lower:
            found_services.append(service)

    # Detect architecture keywords
    found_arch = []
    for keyword in ARCH_KEYWORDS:
        if keyword.lower() in title_lower:
            found_arch.append(keyword)

    # Detect best pattern match
    best_pattern = None
    best_pattern_score = 0
    for pattern, keywords in PATTERN_KEYWORDS.items():
        score = sum(1 for kw in keywords if kw.lower() in title_lower)
        if score > best_pattern_score:
            best_pattern_score = score
            best_pattern = pattern

    # Detect diagram type
    detected_type = "architecture"  # default
    best_type_score = 0
    for dtype, keywords in DIAGRAM_TYPE_KEYWORDS.items():
        score = sum(1 for kw in keywords if kw.lower() in title_lower)
        if score > best_type_score:
            best_type_score = score
            detected_type = dtype

    # Decision: needs azure-diagrams?
    has_cloud = len(found_services) > 0
    has_arch = len(found_arch) > 0

    if has_cloud and has_arch:
        confidence = "high"
        needs_diagram = True
    elif has_cloud or (has_arch and best_pattern_score > 0):
        confidence = "medium"
        needs_diagram = True
    else:
        confidence = "low"
        needs_diagram = False

    return {
        "needs_diagram": needs_diagram,
        "diagram_type": detected_type,
        "suggested_pattern": best_pattern,
        "cloud_services": found_services,
        "arch_keywords": found_arch,
        "confidence": confidence,
    }


def analyze_outline(slides: list) -> list:
    """Analyze all slides and return those needing azure-diagrams."""
    results = []
    for slide in slides:
        detection = detect_cloud_content(slide["title"])
        if detection["needs_diagram"]:
            results.append({
                "slide_index": slide["index"],
                "slide_title": slide["title"],
                "diagram_type": detection["diagram_type"],
                "suggested_pattern": detection["suggested_pattern"],
                "cloud_services": detection["cloud_services"],
                "confidence": detection["confidence"],
            })
    return results


