# =============================================================================
# SLIDE PARSING
# =============================================================================

def parse_markdown_outline(text: str) -> list:
    """Parse a markdown task plan or outline into slide entries."""
    slides = []
    lines = text.strip().split("\n")

    # Pattern 1: "Slide N: Title" or "幻灯片 N: Title"
    slide_pattern = re.compile(
        r"(?:slide|幻灯片|s)\s*(\d+)\s*[:：]\s*(.+)",
        re.IGNORECASE
    )

    # Pattern 2: Numbered list "1. Title" or "- Title"
    list_pattern = re.compile(r"^\s*(?:(\d+)[.)]\s+|- |\* )(.*)", re.IGNORECASE)

    # Pattern 3: Markdown headers "## Title" or "### Title"
    header_pattern = re.compile(r"^#{1,4}\s+(?:(?:slide|幻灯片)\s*\d+\s*[:：]\s*)?(.+)", re.IGNORECASE)

    for line in lines:
        line = line.strip()
        if not line:
            continue

        match = slide_pattern.match(line)
        if match:
            slides.append({
                "index": int(match.group(1)) - 1,
                "title": match.group(2).strip()
            })
            continue

        match = list_pattern.match(line)
        if match:
            idx = int(match.group(1)) - 1 if match.group(1) else len(slides)
            title = match.group(2).strip()
            if title and len(title) > 3:  # skip very short items
                slides.append({"index": idx, "title": title})
            continue

        match = header_pattern.match(line)
        if match:
            title = match.group(1).strip()
            if title and len(title) > 3:
                slides.append({"index": len(slides), "title": title})

    # Deduplicate by index, keep last
    seen = {}
    for s in slides:
        seen[s["index"]] = s
    return sorted(seen.values(), key=lambda x: x["index"])


def parse_json_outline(text: str) -> list:
    """Parse a JSON slide outline."""
    try:
        data = json.loads(text)
    except json.JSONDecodeError:
        return []

    slides = []
    if isinstance(data, list):
        for i, item in enumerate(data):
            if isinstance(item, str):
                slides.append({"index": i, "title": item})
            elif isinstance(item, dict):
                title = item.get("title", item.get("name", item.get("content", "")))
                slides.append({"index": item.get("index", i), "title": str(title)})
    return slides


