def main():
    parser = argparse.ArgumentParser(
        description="Detect slides that need azure-diagrams for cloud architecture",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  %(prog)s --outline task_plan.md
  %(prog)s --outline slides.json
  %(prog)s --text "Slide 1: Azure AI Foundry Architecture\\nSlide 2: Key Benefits"
  %(prog)s --text "1. 系统架构 - Azure Kubernetes + Cosmos DB\\n2. 项目收益"
        """
    )

    parser.add_argument("--outline", "-o", type=str,
                        help="Path to outline file (markdown or JSON)")
    parser.add_argument("--text", "-t", type=str,
                        help="Inline outline text (use \\n for newlines)")
    parser.add_argument("--json", action="store_true",
                        help="Force JSON output (default is human-readable)")

    args = parser.parse_args()

    if not args.outline and not args.text:
        parser.print_help()
        sys.exit(1)

    # Load outline
    if args.outline:
        path = Path(args.outline)
        if not path.exists():
            print(f"Error: File not found: {args.outline}", file=sys.stderr)
            sys.exit(1)
        text = path.read_text(encoding="utf-8")
    else:
        text = args.text.replace("\\n", "\n")

    # Parse
    if text.strip().startswith("[") or text.strip().startswith("{"):
        slides = parse_json_outline(text)
    else:
        slides = parse_markdown_outline(text)

    if not slides:
        print("No slides detected in the outline.", file=sys.stderr)
        print("[]")
        sys.exit(0)

    # Analyze
    results = analyze_outline(slides)

    # Output
    print(json.dumps(results, indent=2, ensure_ascii=False))

    # Summary to stderr
    if results:
        print(f"\n--- Summary ---", file=sys.stderr)
        print(f"Total slides analyzed: {len(slides)}", file=sys.stderr)
        print(f"Slides needing azure-diagrams: {len(results)}", file=sys.stderr)
        for r in results:
            conf = "***" if r["confidence"] == "high" else "**"
            print(f"  [{conf}] Slide {r['slide_index']}: {r['slide_title']}", file=sys.stderr)
            print(f"       Type: {r['diagram_type']}, Pattern: {r['suggested_pattern']}", file=sys.stderr)
            if r["cloud_services"]:
                print(f"       Services: {', '.join(r['cloud_services'])}", file=sys.stderr)
    else:
        print(f"\nNo cloud architecture slides detected. azure-diagrams not needed.", file=sys.stderr)


if __name__ == "__main__":
    main()
